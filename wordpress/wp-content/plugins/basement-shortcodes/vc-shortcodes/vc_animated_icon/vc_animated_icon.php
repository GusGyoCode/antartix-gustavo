class WPBakeryShortCode_vc_animated_icon extends WPBakeryShortCode {

	protected function content( $atts, $content = null ) {

		$icon_sharpicons = $color = $size = $width = $height = $weight = $style = $align = $animate = $el_class = $css_animation = $css = '';


		$atts = vc_map_get_attributes( $this->getShortcode(), $atts );
		extract( $atts );


		$class_to_filter = '';
		$class_to_filter .= vc_shortcode_custom_css_class( $css, ' ' ) . $this->getExtraClass( $el_class ) . $this->getCSSAnimation( $css_animation );
		$css_class = apply_filters( VC_SHORTCODE_CUSTOM_CSS_FILTER_TAG, $class_to_filter, $this->settings['base'], $atts );

		$icon       = '';
		$css_styles = array();

		$size = isset( $size ) ? $size : '';
		$size = str_replace( array(
			'px',
			' ',
		), array(
			'',
			'',
		), $size );

		$width = str_replace( array(
			'px',
			' ',
		), array(
			'',
			'',
		), $width );


		$height = str_replace( array(
			'px',
			' ',
		), array(
			'',
			'',
		), $height );

		$weight = str_replace( array(
			'px',
			' ',
		), array(
			'',
			'',
		), $weight );


		$animate_state = '';
		if ( $animate === 'yes' ) {
			$animate_state = 'vc_is_animate_icon';
		}


		if ( ! empty( $icon_sharpicons ) ) {
			$svg_name = $icon_sharpicons . '.svg';
			$svg_file = BASEMENT_SORTCODES_DIR . 'assets/images/svg/' . $svg_name;
			$clear_name = str_replace(array('.svg','.','si-'),array('','-',''),$svg_name);


			if ( file_exists( $svg_file ) ) {

				$svg_content = file_get_contents( $svg_file );
				if ( $svg_content ) {
					$svg_content = preg_replace( array( "/<!--.*?-->/ms", '/<\?.*?\?>/ms' ), "", $svg_content );
					$id          = uniqid( 'vc-ai-' );


					$array = array();
					preg_match( '/id="([^"]*)"/i', $svg_content, $array );

					if ( empty( $array['1'] ) ) {
						$svg_content = preg_replace( '/<svg /', "<svg id=\"{$clear_name}\"", $svg_content, 1 );
						preg_match( '/id="([^"]*)"/i', $svg_content, $array );
					}

					if ( ! empty( $array['1'] ) ) {
						$id          = $id . '-' . $array['1'];
						$svg_content = preg_replace( '/id="(.*?)"/', "id=\"{$id}\"", $svg_content, 1 );
						$css_class   = strlen( $css_class ) > 0 ? ' ' . trim( esc_attr( $css_class ) ) : '';
						$align       = ! empty( $align ) ? 'text-' . $align : '';
						if ( ! empty( $color ) ) {
							$css_styles[] = "#{$id} * {stroke:{$color} !important;}";
						}


						if ( ! empty( $style ) && $style !== 'round' ) {
							$css_styles[] = "#{$id} path {stroke-linecap:{$style} !important;}";
						}

						if ( strpos( $icon_sharpicons, 'separators' ) !== false ) {

							$overflow_styles = array();

							if ( is_numeric( $height ) ) {
								$overflow_styles[] = "height:{$height}px;";

							}

							if ( is_numeric( $width ) ) {
								$overflow_styles[] = "width:{$width}px;";
							}

							if ( ! empty( $overflow_styles ) ) {
								$overflow_styles = implode( '', $overflow_styles );
								$overflow_styles = "style=\"{$overflow_styles}\"";
							} else {
								$overflow_styles = '';
							}


							if ( is_numeric( $weight ) && $weight != 1 ) {
								$css_styles[] = "#{$id} path {stroke-width:{$weight}px !important;}";
							}
							if ( ! empty( $css_styles ) ) {
								$css_styles  = implode( '', $css_styles );

								$svg_content = preg_replace( '/<\/svg>/', "<style>{$css_styles} </style></svg>", $svg_content, 1 );
							}

							if ( preg_match( "#-h$#", $icon_sharpicons ) ) {
								if ( ! is_numeric( $size ) ) {
									$size = '1200';
								} else {
									$size = round( ( 1200 * $size ) / 7 );
								}
								$svg_content = preg_replace( '/id="/', "width=\"{$size}px\" id=\"", $svg_content, 1 );
							} elseif ( preg_match( "#-v$#", $icon_sharpicons ) ) {

								if ( is_numeric( $height ) ) {
									if ( is_numeric( $size ) ) {
										$size = "width=\"{$size}px\"";
									} else {
										$size = "height=\"{$height}px\"";
									}
									$svg_content = preg_replace( '/id="/', "{$size} id=\"", $svg_content, 1 );
									$svg_content = preg_replace( '/viewBox="0 0 (\d*(?:\.\d+)?) (\d*(?:\.\d+)?)"/', "viewBox=\"0 0 $1 {$height}\"", $svg_content, 1 );
									$svg_content = preg_replace( '/enable-background="new 0 0 (\d*(?:\.\d+)?) (\d*(?:\.\d+)?)"/', "enable-background=\"new 0 0 $1 {$height}\"", $svg_content, 1 );
								} else {
									if ( is_numeric( $size ) ) {
										$size = "width=\"{$size}px\"";
									} else {
										$size = "height=\"1200px\"";
									}
									$svg_content = preg_replace( '/id="/', "{$size} id=\"", $svg_content, 1 );
								}
							}

							$icon = sprintf( '<div class="vc-animated-icon %s %s %s"><div class="vc-overflow-icon" %s>%s</div></div>', $animate_state, esc_attr( $css_class ), esc_attr( $align ), $overflow_styles, $svg_content );
						} else {
							if ( ! is_numeric( $size ) ) {
								$size = '66';
							}

							$svg_content = preg_replace( '/id="/', "width=\"{$size}px\" id=\"", $svg_content, 1 );

							if ( is_numeric( $weight ) && $weight != 2 ) {
								$css_styles[] = "#{$id} path {stroke-width:{$weight}px !important;}";
							}
							if ( ! empty( $css_styles ) ) {
								$css_styles  = implode( '', $css_styles );
								$svg_content = preg_replace( '/<\/svg>/', "<style>{$css_styles} </style></svg>", $svg_content );
							}

							$icon = sprintf( '<div class="vc-animated-icon %s %s %s">%s</div>', $animate_state, esc_attr( $css_class ), esc_attr( $align ), $svg_content );
						}

					}

				}
			}
		}

		return $icon;
	}
}

